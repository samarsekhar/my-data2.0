/* What is React?
   React sometimes referred to as a frontend javascript framework, is a javascript library created by Facebook.
   React is a tool for building UI components.
*/

/* React Work ?
   React creates a VIRTUAL DOM in memory.

   Instead of manipulating the browser's DOM directly, React create a virtual DOM in memory, where it
   does all the necessary manipulating, before making the changes in the browser DOM.

   React only changes what needs to be changed!

*/

/* What is ES6 ?
   ES6 stands for ECMAScript 6.
   ECMAScript was created to standardize JavaScript, and ES6 is the 6th version of ECMAScript, it was
   published in 2015, and is also known as ECMAScript 2015.

   Why Should Learn ES6 ?
   React uses ES6, and you should be familiar with some of the new features Like:-
   Classes, Arrow Functions, Variables(let,const,var), Array Methods like .map(), Destructuring, Modules, Ternary Operator, Spread Operator.
*/

/* ES6 Classes ?
   A class is a type of function, but instead of using the keyword 'function' to initiate it, we use the keyword
   'class', and the properties are assigned inside a constructor() method.

   Note: The constructor function is called automatically when the object is initialized.

   Class Inheritance ?
   To create a class inheritance, use the extends keyword.
   A class created with a class inheritance inherits all the methods from another class.
   
   The super() method refers to the parent class.
   By calling the super() method in the constructor method, we call the parent's constructor method and
   get access to the parent's properties and methods. 

   
*/